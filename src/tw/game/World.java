    public void run() {
        while(true) {
            synchronized(this) {
                while(isChanging) {
                    try {
                        this.wait();
                    }
                    catch(InterruptedException e) {
                        e.printStackTrace();
                    }
                }
                repaint();
                sleep(1);
                for (int i = 0; i < ROW; i++) {
                    for (int j = 0; j < COL; j++) {
                        nextGeneration(i, j);
                    }
                }
                currentWorld = nextWorld;

            }
        }
    }

    private boolean isVaild(int x,int y){
        if((x >= 0 && x < ROW) && (y >=0 && y < COL)){
            return true;
        }
        return false;
    }

    private int getState(int x, int y) {
        if(isVaild(x, y)&&(currentWorld[x][y]==CellState.Alive)) {
            return 1;
        }
        return 0;
    }

    private void nextGeneration(int x, int y) {
        int countState = 0;
        countState += getState(x-1, y-1);
        countState += getState(x, y-1);
        countState += getState(x+1, y-1);
        countState += getState(x+1, y);
        countState += getState(x+1, y+1);
        countState += getState(x, y+1);
        countState += getState(x-1, y+1);
        countState += getState(x-1, y);

        if(countState == 3) {
            nextWorld[x][y] = CellState.Alive;
        }
        else if(countState == 2){
            nextWorld[x][y] = currentWorld[x][y];
        }
        else {
            nextWorld[x][y] = CellState.Dead;
        }

    }

    private void sleep(int secods) {
        try {
            if(secods <= 0) {
                secods = 1;
            }
            Thread.sleep(1000 * secods);
        } catch (InterruptedException e) {
            e.printStackTrace();
        }
    }

    public void setArrow() {
        setShape(arrow);
    }

    public void setSquare() {
        setShape(square);
    }

    public void paintComponent(Graphics g) {
        super.paintComponent(g);
        for(int i = 0;i < ROW;i++) {
            for(int j = 0;j < COL;j++) {
                if(currentWorld[i][j] == CellState.Alive) {
                    g.fillRect(j*20, i*20, 20, 20);
                }
                else {
                    g.drawRect(j*20, i*20, 20, 20);
                }
            }
        }
    }

    private void setShape(int [][]shape) {
        isChanging = true;
        int arrowsRows = shape.length;
        int arrowsColumns = shape[0].length;
        //限定尺寸，保证图形的长宽不会超过虚拟世界的范围
        int minimumRows = (arrowsRows < ROW) ? arrowsRows : ROW;
        int minimunColumns = (arrowsColumns < COL) ? arrowsColumns : COL;

        synchronized(this) {
            //虚拟世界初始状态中，所有cell状态均为dead
            for(int i = 0;i < ROW;i++) {
                for(int j = 0;j < COL;j++) {
                    currentWorld[i][j] = CellState.Dead;
                }
            }

            for(int i = 0;i < minimumRows;i++) {
                for(int j = 0;j < minimunColumns;j++) {
                    if(shape[i][j] == 1) {
                        currentWorld[i][j] = CellState.Alive;
                    }
                }
            }
            isChanging = false;
            this.notifyAll();
        }
    }

